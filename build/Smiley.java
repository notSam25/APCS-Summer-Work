package build;
import java.util.Scanner;

class Smiley {
  public static Scanner scanner = new Scanner(System.in);

  public static void main(String[] args) {
    System.out.print  ("-----------\n");
    System.out.print  ("--()---()--\n");
    System.out.println("----||-----");
    System.out.println("(_________)");
    System.out.println("-----------");
    scanner.close();
  }
}